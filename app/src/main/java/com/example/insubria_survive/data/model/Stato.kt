package com.example.insubria_survive.data.model

/**
 * Enum che definisce i possibili stati di una preferenza per un esame.
 */
enum class Stato {
    /** Stato che indica che l'azione relativa all'esame è da fare */
    DA_FARE,
    /** Stato che indica che l'azione relativa all'esame è in forse */
    IN_FORSE,
    /** Stato che indica che l'azione relativa all'esame non è da fare */
    NON_FARE
}
